/**
* _isdigit - entry point
* @c: the letter the check is conducted
* main - entry point
* Description: A function  that checks for digits,
* int _islower(int c): returns 1 if true and 0 if false.
* Return: 0
*/
int _isdigit(int c)
{
if (c >= '0' && c <= '9')
	return (1);
else
return (0);

}
