#include "main.h"
#include <limits.h>
/**
* print_last_digit - entry point
* @n: the integer that is use to determine the last digit
* Description: A function that computes the absolute value of an integer
* Return: 0
*/
int print_last_digit(int n, m)
{
	n = INT_MIN;
	m = n%10;
	_putchar ('0'- m);

_putchar('\n');
return (m);
}
